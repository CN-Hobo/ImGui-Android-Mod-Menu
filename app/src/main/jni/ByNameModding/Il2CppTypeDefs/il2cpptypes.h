struct monoString {
    void *klass;
    void *monitor;
    int length;
    char chars[255];

    std::string get_string() {
        std::u16string u16_string(reinterpret_cast<const char16_t *>(chars));
        std::wstring wide_string(u16_string.begin(), u16_string.end());
        std::wstring_convert<std::codecvt_utf8_utf16<wchar_t>> convert;
        return convert.to_bytes(wide_string);
    }

    char *getChars() {
        return chars;
    }

    int getLength() {
        return length;
    }

};

template<typename T>
struct monoArray {
    void *klass;
    void *monitor;
    void *bounds;
    int32_t max_length;
    T m_Items[65535];

    int32_t getLength() {
        return max_length;
    }

    T *getPointer() {
        return m_Items;
    }
};

template<typename T>
struct monoList {
    void *klass;
    void *monitor;
    monoArray<T> *Items;
    int32_t size;
    int32_t version;
    void *syncRoot;

    T* getItems() {
        return Items->getPointer();
    }

    int32_t getSize() {
        return size;
    }

    int32_t getVersion() {
        return version;
    }

    void Add(T val) {
        Items->m_Items[size] = val;
        size++;
        ++version;
    }
};
